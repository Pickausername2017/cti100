def main ():
        import turtle
        for i in [0,1,2,3,]: #Draws a square
                turtle.forward(50)
                turtle.left(90)

                
        for i in [0,1,2]:
                turtle.forward(80) #Draws a triangle
                turtle.left(120)
        
	
	
      
                
	
                
main() 

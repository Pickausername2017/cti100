def main():

    import turtle
    wn = turtle.Screen()
    wn.bgcolor('green')
    ashley = turtle.Turtle()
    ashley.color ('pink')
    ashley.pensize (5)

    ashley.left(80)
    ashley.forward(100)
    ashley.right(140)
    ashley.forward(100)
    ashley.backward(45)
    ashley.right(100)
    ashley.forward(45)
    ashley.left(180) 

    ashley.penup()
    ashley.forward(100)
    ashley.pendown ()
    

    ashley.forward(50)
    ashley.right(90)
    ashley.forward(50)
    ashley.right(90)
    ashley.forward(50)
    ashley.right(90)
    ashley.forward(50)
    ashley.right(360)
    ashley.forward(50)
    ashley.right(90)
    ashley.forward(50)
    ashley.right(90)
    ashley.forward(50)
    ashley.right(90)
    ashley.forward(50)
    
main() 

def main ():
        import turtle
        for i in [0,1,2,3,]:
                turtle.forward(50)
                turtle.left(90)
	
	
        for i in [0,1]:
                turtle.forward(50)
                turtle.right(60)
        for i in [2]:
                turtle.right(90)
                turtle.forward(100)
	
                
main() 
